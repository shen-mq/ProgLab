#include <iostream>
using namespace std;

int main() {
	double r, h;

	cout << "Please input the radius and height of a cylinder: " ;
	cin >> r >> h;

	cout << "底面积 = " << 3.1415927 * r * r << " m^2" << endl;
	cout << "体  积 = " << 3.1415927 * r * r * h << " m^3" << endl;
	cout << "表面积 = " << 3.1415927 * r * r * 2 +  2 * 3.14159266 * r * h << " m^2" << endl;

	return 0;
}


