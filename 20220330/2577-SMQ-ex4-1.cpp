#include <iostream>
using namespace std;

int main() {
	int n, k;
	for (n = 1; n <= 200; ++n) {
		for (k = 1; k * k <= n; ++k) {
			if (k * k == n)
				cout << n << "   ";
		}
	}
	cout << endl;

	return 0;
}
